import lnk from 'lnk'
import fs from 'node:fs'

//console.log(process.cwd())
fs.lstat('node_modules/storage-plugins', function(err, stats) {
    if( err && err.code === 'ENOENT' ){
        lnk("__test__/test_node_modules/storage-plugins","node_modules")
    }
    else {
        if( !stats.isSymbolicLink() ){
            lnk("__test__/test_node_modules/storage-plugins","node_modules")
        }
    }
})

